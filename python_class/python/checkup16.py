word = input()
cnt = 0

for i in word:
    if i == 'a':
        cnt += 1
        continue
    elif i == 'e':
        cnt += 1
        continue
    elif i == 'i':
        cnt += 1
        continue
    elif i == 'o':
        cnt += 1
        continue
    elif i == 'u':
        cnt += 1
        continue
print(cnt)
    

